// ============== HELPER: Create TipTap content ==============

function text(t: string): JSONContent {
  return { type: "text", text: t };
}

function bold(t: string): JSONContent {
  return { type: "text", text: t, marks: [{ type: "bold" }] };
}

function link(t: string, href: string): JSONContent {
  return { type: "text", text: t, marks: [{ type: "link", attrs: { href } }] };
}

function paragraph(...content: JSONContent[]): JSONContent {
  return { type: "paragraph", content };
}

function heading(level: number, t: string): JSONContent {
  return {
    type: "heading",
    attrs: { level },
    content: [text(t)],
  };
}

function bulletList(...items: string[]): JSONContent {
  return {
    type: "bulletList",
    content: items.map((item) => ({
      type: "listItem",
      content: [paragraph(text(item))],
    })),
  };
}

function table(headers: string[], rows: string[][]): JSONContent {
  return {
    type: "table",
    content: [
      {
        type: "tableRow",
        content: headers.map((h) => ({
          type: "tableHeader",
          content: [paragraph(text(h))],
        })),
      },
      ...rows.map((row) => ({
        type: "tableRow",
        content: row.map((cell) => ({
          type: "tableCell",
          content: [paragraph(text(cell))],
        })),
      })),
    ],
  };
}

